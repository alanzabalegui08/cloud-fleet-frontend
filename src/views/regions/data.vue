<template>
    <div class="row p-5" >
        <div class="col">
            <h2 >Regiones</h2>
        </div>
        <div class="col text-right">
            <router-link to="/regiones/nuevo" class="btn cur-p btn-outline-primary">Nuevo</router-link>
        </div>
        <div class="col-xs-12 col-md-12">
            <div class="bgc-white p-10 bd">
                <div class="mT-10">
                    <div class="row">
                        <div class="col-md-12">
                            <table-klay :list="regions" :columns="dataHeader" @onDeleteItem="onDeleteItem"></table-klay>
                        </div>
                    </div>
                </div>
            </div>
        </div>
    </div>
</template>
<script>
import { mapGetters, mapActions } from 'vuex';
import $ from 'jquery';
import TableKlay from '@/components/Table';
import DataHeader from '@/util/datasource.js';
import { alerts } from '@/util/alerts';

export default {
    name : 'data',
    mixins : [ alerts ],
    components : {
        TableKlay,
    },
    data () {
        return {
            name : 'region',
            dataHeader : [],
        }
    },
    created (){
        this.dataHeader = DataHeader.region;
    },
    computed : {
        ...mapGetters({
            regions: 'region/getRegions'
        })
    },
    methods : {
        ...mapActions({
            'remove' : 'region/removeRegion'
        }),

        showModal() {
            let element = this.$refs.modal.$el;
            $(element).modal('show');
        },

        onDeleteItem(item) {
            this.deleteResource(this.name).then(
                (result) => {
                    if(result){
                        this.remove(item);
                    }
                }
            );
        }
    }
}
</script>